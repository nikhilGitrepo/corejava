package datatype;

public class Multiplication {

	public static void main(String[] args) {
		int a =1;
		double d = 1.0;
		float f = 1.0f;
		
		double res1 = a*d;
		float res2 = a*f;
		double res3 = d*f;
		
		double res4 = f*d;
		
		
		System.out.println("------------------------");
		
		System.out.println(res1);
		System.out.println(res2);
		System.out.println(res3);
		System.out.println(res4);
		
		System.out.println("------------------------");
	
	}
	
}
